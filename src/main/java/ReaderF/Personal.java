package ReaderF;

public class Personal {
    public String country;
    public String city;
    public char gender;
    public CurrencyType currencyType;
    public Double avgIncome;

    public Personal(String country, String city, char gender, CurrencyType currencyType, Double avgIncome) {
        this.country = country;
        this.city = city;
        this.gender = gender;
        this.currencyType = currencyType;
        this.avgIncome = avgIncome;

    }

}

